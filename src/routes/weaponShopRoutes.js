// weaponShopController.js, weaponShopModel.js, weaponShopRoutes.js is for managing the weapon shop and for buying weapons

const express = require("express");
const router = express.Router();
const controller = require("../controllers/weaponShopController");

//////////////////////////////////////////////////////////////
// 1 - GET all weapons in shop
//////////////////////////////////////////////////////////////
router.get("/", controller.readAllWeapons);

//////////////////////////////////////////////////////////////
// 2 - GET all weapons in shop by class
//////////////////////////////////////////////////////////////
router.get("/:class", controller.readAllWeaponsByClass);

//////////////////////////////////////////////////////////////
// 3 - POST new weapon and minus respective skillpoints of selected weapon by weapon_id weapon
//////////////////////////////////////////////////////////////
router.post(
  "/:user_id",
  controller.checkUserExistence,
  controller.checkWeaponExistence,
  controller.deductUserSkillpoints,
  controller.updateInventory
);

///// exporting /////
module.exports = router;

