// gacha system

const express = require("express");
const router = express.Router();
const controller = require("../controllers/characterController");

//////////////////////////////////////////////////////////////
// 1 - GET all characters in character pool
//////////////////////////////////////////////////////////////
router.get("/", controller.readAllCharactersInCharacterPool);

//////////////////////////////////////////////////////////////
// 2 - GET character in character pool by class
//////////////////////////////////////////////////////////////
router.get("/:class", controller.readAllCharactersInCharacterPoolByClass);

//////////////////////////////////////////////////////////////
// 3 - POST random character into UserHeroes and deduct cost of drawing character from character pool
//////////////////////////////////////////////////////////////
router.post(
  "/:user_id",
  controller.checkUserExistence,
  controller.deductUserSkillpoints,
  controller.getRandomCharacter
);

///// exporting /////
module.exports = router;

